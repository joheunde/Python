# 전통적인 언어들(C, Java)은 변수를 선언할 때 자료형도 함께 선언
# 예시) int var1 = 10;
# 파이썬은 변수를 선언할 때 자료형을 선언하지 않음
# 또한 같은 변수명에 여러 자료형의 값을 대입할 수 있음

# 정수형(int)
# 전통적인 언어들과는 달리 자릿수에 제한이 없다
n1 = 2**256 - 1
print(n1)
print(type(n1))

n2 = 4 / 2
print(n2)
